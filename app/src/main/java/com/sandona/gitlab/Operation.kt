package com.sandona.gitlab

class Operation {

    fun add(num1:Int,num2:Int):Int{
        return num1+num2;
    }
    fun subtraction(num1:Int,num2:Int):Int{
        return num1-num2;
    }
    fun multiply(num1:Int,num2:Int):Int{
        return num1*num2;
    }
    fun division(num1:Int,num2:Int):Int{
        return num1/num2;
    }
    fun modulo(num1:Int,num2:Int):Int {
        return num1 % num2;
    }
}